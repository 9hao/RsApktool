package complile.obfascator;

public class Trap {

	private String rawClassName;
	private String changeClassName;
	private String finalClassPath;
	
	private String conciseIdentity;
	private int    obfascatorNum;
	
	
	
	
	public int getObfascatorNum() {
		return obfascatorNum;
	}
	public void setObfascatorNum(int obfascatorNum) {
		this.obfascatorNum = obfascatorNum;
	}
	public String getConciseIdentity() {
		return conciseIdentity;
	}
	public void setConciseIdentity(String conciseIdentity) {
		this.conciseIdentity = conciseIdentity;
	}
	public String getFinalClassPath() {
		return finalClassPath;
	}
	public void setFinalClassPath(String finalClassPath) {
		this.finalClassPath = finalClassPath;
	}
	public String getRawClassName() {
		return rawClassName;
	}
	public void setRawClassName(String rawClassName) {
		this.rawClassName = rawClassName;
	}
	public String getChangeClassName() {
		return changeClassName;
	}
	public void setChangeClassName(String changeClassName) {
		this.changeClassName = changeClassName;
	}

	
	
	
	
}
